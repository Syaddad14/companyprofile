        $features = Features::find(1);
        if($request->file('image')){
            $file = $request->file('image');

            $nama_file = time()."_".$file->getClientOriginalName();

            $tujuan_upload = 'gambar';
            $file->move($tujuan_upload,$nama_file);

        $features->update([
            'image' => $nama_file,
        ]);
        }
           
        $features->update();
        return redirect('feature');
